# LAMBDA EXPRESSIONS
# ANONYMOUS FUNCTIONS
# SINGLE-LINE FUNCTIONS

#funtion_name = lambda par1, par2, .. : expression

def sum(num1, num2):
    sum = num1 + num2
    return sum

sum2 = lambda num1, num2: num2 + num2

print(sum2(5,6))
